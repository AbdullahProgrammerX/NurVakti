  final String recipeDetail;

  const DailyContent({
    required this.verse,
    required this.verseSource,
    required this.hadith,
    required this.hadithSource,
    required this.risaleQuote,
    required this.risaleSource,
    required this.historyToday,
    required this.historyYear,
    required this.recipe,
    required this.recipeDetail,
  });
}

/// 366 günlük içerik veritabanı (günün indeksine göre)
final dailyContentProvider = Provider.family<DailyContent, DateTime>((ref, date) {
  final dayOfYear = _dayOfYear(date);
  final index = dayOfYear % _contentDatabase.length;
  return _contentDatabase[index];
});

int _dayOfYear(DateTime date) {
  return date.difference(DateTime(date.year, 1, 1)).inDays;
}

